		<div>
			<p>You have questions, we have answers. The list bellow shows the most common questions people ask about Mentaway.<br />
				In case your question is not listed here, don't worry, send an email to <a href="mailto:help@mentaway.com">help@mentaway.com</a> and we will help you out.
