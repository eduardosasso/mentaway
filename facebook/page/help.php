	<article>
		<h1>What is Mentaway?</h1>
		<div>
			<p>Mentaway is a service that help people to organize travel related information to make easier to share them with friends and family.</p>
		</div>
	</article>
		
	<article>
		<h1>How does Mentaway work?</h1>
		<div>
			<p>Mentaway is based on geo located information, so we take info from services like <a href="http://foursquare.com/">Foursquare</a>, <a href="http://twitter.com/">Twitter</a> and <a href="http://www.flickr.com/">Flickr</a> and automatically organize them in a map. </p>
		</div>
	</article>
	
	<article>
		<h1>Is the service strictly related to travel? </h1>
		<div>
			<p>Mentaway will actually show all your geo located info from other services, so it will manage both daily information and travel information. It will basically organize every step you take, at your city and abroad. </p>
		</div>
	</article>
	
	<article>
		<h1>How do I show a photo I post on Twitter on Mentaway?</h1>
		<div>
			<p>When you send your Tweet with the pic use #m and don't forget to enable geo location on your Twitter settings. We support pictures sent via <a href="http://twitpic.com/">Twitpic</a>, <a href="http://yfrog.com/">Yfrog</a> and <a href="http://plixi.com/">Plixi</a>.  </p>
		</div>
	</article>

	<article>
		<h1>Is it true that I can have my <a href="http://instagr.am/">Instagram</a> pics on my Mentaway map?</h1>
		<div>
			<p>Yeap, this is true. You just need to add Flickr and/or Twitter to your Mentaway services and when you share a pic on Instagram choose the share also via Flickr and/or Twitter option (don't forget to enable geo location on your Twitter settings). Your pic will appear on Mentaway.   </p>
		</div>
	</article>
	
	<article>
		<h1>Talking about Flickr, how is the Flickr - Mentaway integration?</h1>
		<div>
			<p>As our service is geo located based you need to geo locate your Flickr photos and they will appear on your Mentaway map. Remember that only the photos updated inside your trip dates will appear.  </p>
		</div>
	</article>
	
	<article>
		<h1>And regarding Foursquare, how does it works?</h1>
		<div>
			<p>Easy one... just check in wherever you want and your check ins will go to your map. :) </p>
		</div>
	</article>
	
	<article>
		<h1>How can I manually add places I've visited?</h1>
		<div>
			<p>As we are a geo location based service, you will need an iPhone, android or other mobile with that capability to use the service. So manual inputs are not enabled.</p>
		</div>
	</article>
	
	<article>
		<h1>Can I use Mentaway only to check out where my friends are?</h1>
		<div>
			<p>Yes you can! Just use the service to browse what your friends are up to. Your map will be empty but you will still be able to check out your friends status. ;) </p>
		</div>
	</article>
	
	<article>
		<h1>How come there is no iPhone app. Any plans on that?</h1>
		<div>
			<p>We are certainly thinking about the iPhone app, it is just a matter of time, so this will be available soon.</p>
		</div>
	</article>
	
	<article>
		<h1>Why the name of the service is Mentaway?</h1>
		<div>
			<p>The name Mentaway was chosen in honor to the Mentawai island, a surfer's paradise in Indonesia, a place that it's in our dreams for a long time and a destination we definitely want to go. </p>
