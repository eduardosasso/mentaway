	<article>
		<h1>What is Mentaway?</h1>
		<div>
			<p>Mentaway is a service that help people to organize travel related information to make easier to share them with friends and family.</p>
		</div>
	</article>
		
	<article>
		<h1>How does Mentaway works?</h1>
		<div>
			<p>Mentaway is based on geolocated information, so we take info from services like <a href="http://foursquare.com/">Foursquare</a>, <a href="http://twitter.com/">Twitter</a> and <a href="http://www.flickr.com/">Flickr</a> and automatically organize them. </p>
		</div>
	</article>
	
	<article>
		<h1>What do I need to use Mentaway and to start populating my timeline? </h1>
		<div>
			<p>You need an iPhone, Android or other mobile with GPS capabilities. And you also need to use one of the services we support.  </p>
		</div>
	</article>
	
	<article>
		<h1>What information will appear at the timeline tab? </h1>
		<div>
			<p>The timeline will contain information from you and your friends, organized by the most recent checkins, pictures, etc. </p>
		</div>
	</article>
	
	<article>
		<h1>How can I manually add places I've visited?</h1>
		<div>
			<p>At this moment you can't. As we are a geolocation based service, you will need an iPhone, Android or other mobile with GPS capabilities to use the service. </p>
		</div>
	</article>
	
	<article>
		<h1>I'm a bit new to all this... what is geolocation?</h1>
		<div>
			<p>Geolocation is the identification of the real-world geographic location of an object, such as a cell phone or an Internet-connected computer terminal. <a href="http://en.wikipedia.org/wiki/Geolocation">(via Wikipedia)</a> </p>
		</div>
	</article>
	
	<article>
		<h1>Is the service strictly related to travel? </h1>
		<div>
			<p>Mentaway will actually show all your checkins, geolocated tweets and flickr photos, so it will manage both daily stuff (when you go to a local restaurant for example) and travel information. It will basically organize every step you take, at your city and abroad. </p>
		</div>
	</article>
	
	<article>
		<h1>How do I show a photo I post on Twitter on Mentaway?</h1>
		<div>
			<p>When you send your tweet with the picture use <strong>#m</strong> and don't forget to <a href="http://support.twitter.com/entries/122236">enable geolocation</a> on your Twitter settings. We support pictures sent via <a href="http://twitpic.com/">Twitpic</a>, <a href="http://yfrog.com/">Yfrog</a> and <a href="http://plixi.com/">Plixi</a>.  </p>
		</div>
	</article>
	
	<article>
		<h1>Do I really need to use #m on my tweets?</h1>
		<div>
			<p>Yes, you will need the #m to show your tweets on Mentaway - otherwise we are not able to know which tweets you want to share with us. And always remember to <a href="http://support.twitter.com/entries/122236">enable geolocation</a> on your <a href="http://twitter.com/">Twitter</a> settings.  </p>
		</div>
	</article>

	<article>
		<h1>Can I have my Instagram pics on my Mentaway?</h1>
		<div>
			<p>Yeap! You just need to add <a href="http://www.flickr.com/">Flickr</a> and/or <a href="http://twitter.com/">Twitter</a> to your Mentaway services and when you share a pic on <a href="http://instagr.am/">Instagram</a> choose the share also via Flickr and/or Twitter option (don't forget to <a href="http://support.twitter.com/entries/122236">enable geolocation</a> on your Twitter settings).   </p>
		</div>
	</article>
	
	<article>
		<h1>How is the Flickr - Mentaway integration?</h1>
		<div>
			<p>As our service is geolocated based you need to geo locate your <a href="http://www.flickr.com/">Flickr</a> photos for them to appear on your Mentaway map.  </p>
		</div>
	</article>
	
	<article>
		<h1>Can I show old Flickr photos on Mentaway?</h1>
		<div>
			<p>Yes, as long as you geolocate them.</p>
		</div>
	</article>
	
	<article>
		<h1>Regarding Foursquare, how does it works?</h1>
		<div>
			<p>Easy one... just checkin wherever you want and your checkins will automatically go to your timeline. :) </p>
		</div>
	</article>

	<article>
		<h1>I don't use any of this services, can I use Mentaway only to check out where my friends are?</h1>
		<div>
			<p>Yes you can! Just use the service to browse what your friends are up to. ;) </p>
		</div>
	</article>
	
	<article>
		<h1>Who are my Mentaway friends? Can I invite people to join the service?</h1>
		<div>
			<p>Your Mentaway friends are you Facebook friends. If your friends also use the service, you will be automatically friends. And you certainly can invite people, just go nuts at the "invite more friends to join" button at the Friends tab. ;)</p>
		</div>
	</article>
	
	<article>
		<h1>Does Mentaway organize checkins in a trip concept?</h1>
		<div>
			<p>Not at the moment, but we will implement this feature in the next version.</p>
		</div>
	</article>
	
	<article>
		<h1>Will Mentaway support other services in the future?</h1>
		<div>
			<p>Yes. For a future version we will include other geolocation based services. </p>
		</div>
	</article>
	
	<article>
		<h1>What is the easiest way to populate my Mentaway?</h1>
		<div>
			<p>The easiest way is using apps of the supported services on your mobile. For example, you can use <a href="http://instagr.am/">Instagram</a>, which is a perfect app for taking cool pictures and sharing them with your friends.   </p>
		</div>
	</article>
	
	<article>
		<h1>I'm traveling or I'm in a place with no internet connection, what should I do?</h1>
		<div>
			<p>You can take pictures and upload them to <a href="http://www.flickr.com/">Flickr</a> when you are connected, then just geolocate the photos and your done. ;) </p>
		</div>
	</article>
	
	<article>
		<h1>How come there is no iPhone/iPad app for the service? Any plans on that?</h1>
		<div>
			<p>We are certainly thinking about the iPhone/iPad app, it is just a matter of time, so this will be available soon.</p>
		</div>
	</article>
	
	<article>
		<h1>Why the name of the service is Mentaway?</h1>
		<div>
			<p>The name Mentaway was chosen in honor to the Mentawai island, a surfer's paradise in Indonesia and a destination we definitely want to go. Also because Mentaway recalls the idea of "meant to be away". </p>
		</div>
	</article>
	
